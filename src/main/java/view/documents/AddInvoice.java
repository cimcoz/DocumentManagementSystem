    private void jButton_deleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_deleteActionPerformed
        int red = jTable_orderItems.getSelectedRow();
        if (red >= 0) {
            OrderItemTableModel model = (OrderItemTableModel) jTable_orderItems.getModel();
            model.deleteOrderItem(red);
        } else {
            JOptionPane.showMessageDialog(this, "Please choose an item!");
        }
    }//GEN-LAST:event_jButton_deleteActionPerformed

    private void jButton_saveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_saveActionPerformed
        try {
            OrderItemTableModel model = (OrderItemTableModel) jTable_orderItems.getModel();
            Invoice invoice = model.getInvoice();
            //invoice.setInvoiceID(Integer.parseInt(jTextField_invoiceID.getText().trim()));
            invoice.setInvoiceDate(df.parse(jTextField_date.getText().trim()));
            invoice.setCustomer((Customer) jComboBox_customer.getSelectedItem());
            int bpid = (Integer.parseInt(jTextField_busPartner.getText().trim()));
            Businesspartner bp = new Businesspartner(bpid);
            invoice.setBp(bp);
            Controller.getInstance().saveInvoice(invoice);
            JOptionPane.showMessageDialog(this, "Invoice saved!");
            jButton_update.setEnabled(true);

            jTextField_id.setText("" + invoice.getInvoiceID());
            //jTextField_invoiceID.setEnabled(false);
            jButton_save.setEnabled(false);
        } catch (Exception ex) {
            ex.printStackTrace();
            JOptionPane.showMessageDialog(this, ex.getMessage());
        }
    }//GEN-LAST:event_jButton_saveActionPerformed

