private void srediFormu() throws Exception {

        try {
            Users user = Controller.getInstance().getLogged();
            String logged = user.getUsername();
            Businesspartner bpid = user.getBp();
            String relatedbp = bpid.getBpID().toString();
            jTextField_createdBy.setText(logged);
            jTextField_busPartner.setText(relatedbp);
            //int bp = Integer.parseInt(jTextField_busPartner.getText());

            List<Customer> bpp = Controller.getInstance().getCustomerListConditional(bpid);
            jComboBox_customer.setModel(new DefaultComboBoxModel(bpp.toArray()));
            OrderItemTableModel model = new OrderItemTableModel(new Invoice());
            jTable_orderItems.setModel(model);

            List<Products> proList = Controller.getInstance().getProductList();
            JComboBox products = new JComboBox(proList.toArray());
            TableColumn tcProizvod = jTable_orderItems.getColumnModel().getColumn(0);
            tcProizvod.setCellEditor(new DefaultCellEditor(products));
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage());
            ex.printStackTrace();
        }

    }
