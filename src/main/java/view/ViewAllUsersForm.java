    private void jButton_deleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_deleteActionPerformed
        int red = jTable_allUsers.getSelectedRow();
        if (red == -1) {
            JOptionPane.showMessageDialog(this, "Please select user!");
        } else {

            UsersTableModel utm = (UsersTableModel) jTable_allUsers.getModel();
            String id = utm.getValueAt(red, 0).toString();
            int userID = Integer.parseInt(id);

            try {
                Controller.getInstance().deleteUser(userID);

                JOptionPane.showMessageDialog(ViewAllUsersForm.this, "User Deleted!");
                utm.deleteRow(red);
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(ViewAllUsersForm.this, "User NOOOOT Deleted!!");
            }
        }
    }//GEN-LAST:event_jButton_deleteActionPerformed

