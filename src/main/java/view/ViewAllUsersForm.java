    private void jButton_UpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_UpdateActionPerformed
        int red = jTable_allUsers.getSelectedRow();
        if (red == -1) {
            JOptionPane.showMessageDialog(this, "Please select user!");
        } else {
            UsersTableModel utm = (UsersTableModel) jTable_allUsers.getModel();
            Users u = utm.getUser(red);
            String id = utm.getValueAt(red, 0).toString();
            String name = utm.getValueAt(red, 1).toString();
            String lastname = utm.getValueAt(red, 2).toString();
            String username = utm.getValueAt(red, 3).toString();
            // String password = utm.getValueAt(red, 4).toString();
            String role = utm.getValueAt(red, 4).toString();
            String pos = utm.getValueAt(red, 5).toString();
            String bp = utm.getValueAt(red, 6).toString();
            updateRow.setVisible(true);
            updateRow.pack();
            updateRow.setDefaultCloseOperation(UpdateUser.DISPOSE_ON_CLOSE);
            this.dispose();

            updateRow.jTextField_userID.setText(id);
            updateRow.jTextField_name.setText(name);
            updateRow.jTextField_lastName.setText(lastname);
            updateRow.jTextField_username.setText(username);
           updateRow.jComboBox_role.setSelectedItem(role);
            updateRow.jComboBox_pos.setSelectedItem(pos);
            updateRow.jComboBox_role.setSelectedItem(role);
            updateRow.jComboBox_bp.setSelectedItem(bp);
        }
    }//GEN-LAST:event_jButton_UpdateActionPerformed

