    public void srediFormu() throws Exception {

        try {
            List<Users> u = Controller.getInstance().getUserList();

            UsersTableModel model = new UsersTableModel(u);
            jTable_allUsers.setModel(model);
        } catch (Exception ex) {

            JOptionPane.showMessageDialog(this, ex.getMessage());

        }

        try {
            java.util.List<Role> rc = Controller.getInstance().getRoleList();
            JComboBox roles = new JComboBox();
            jComboBox_role.removeAllItems();
            for (Role c : rc) {
                jComboBox_role.addItem(c);
                roles.addItem(c);
            }

        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage());
            ex.printStackTrace();
        }

        try {
            java.util.List<Positions> lp = Controller.getInstance().getPositionList();
            JComboBox posi = new JComboBox();
            jComboBox_pos.removeAllItems();
            for (Positions p : lp) {
                jComboBox_pos.addItem(p);
                posi.addItem(p);
            }

        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage());
            ex.printStackTrace();
        }

        try {
            java.util.List<Businesspartner> bp = Controller.getInstance().getPartnerList();
            JComboBox posi = new JComboBox();
            jComboBox_businessPartner.removeAllItems();
            for (Businesspartner p : bp) {
                jComboBox_businessPartner.addItem(p);
                posi.addItem(p);
            }

        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage());
            ex.printStackTrace();
        }

    }

