    public void srediFormu() throws Exception {

        try {
            List<Businesspartner> bp = Controller.getInstance().getPartnerList();
            //jComboBox_partnerList.setModel(new DefaultComboBoxModel(bp.toArray()));
            BusinessPartnerTableModel model = new BusinessPartnerTableModel(bp);
            jTable_viewAllBusPartners.setModel(model);
        } catch (Exception ex) {

            ex.printStackTrace();
            JOptionPane.showMessageDialog(this, ex.getMessage());

        }

        try {
            java.util.List<City> lc = Controller.getInstance().getCityList();
            JComboBox cityTable = new JComboBox();
            jComboBox_partnerCity.removeAllItems();
            for (City c : lc) {
                jComboBox_partnerCity.addItem(c);
                cityTable.addItem(c);
            }

        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage());
        }

    }

