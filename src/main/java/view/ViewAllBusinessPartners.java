    private void jButton_deleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_deleteActionPerformed

        int red = jTable_viewAllBusPartners.getSelectedRow();
        if (red == -1) {
            JOptionPane.showMessageDialog(this, "Please select business partner!");
        } else {

            BusinessPartnerTableModel model = (BusinessPartnerTableModel) jTable_viewAllBusPartners.getModel();

            String id = model.getValueAt(red, 0).toString();
            int bpid = Integer.parseInt(id); 
            try {
                Controller.getInstance().deletePartner(bpid);

                JOptionPane.showMessageDialog(ViewAllBusinessPartners.this, "Business Partner Deleted!");
                model.deleteRow(red);
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(ViewAllBusinessPartners.this, "Business Partner NOOOOT Deleted!!");
            }
        }
    }//GEN-LAST:event_jButton_deleteActionPerformed

